export function calculateRatingStats(reviews) {
  const stats = { 5: 0, 4: 0, 3: 0, 2: 0, 1: 0 };
  const total = reviews.length;

  reviews.forEach((review) => {
    stats[review.rating]++;
  });

  return {
    totalReviews: total,
    averageRating:
      total > 0
        ? (reviews.reduce((sum, r) => sum + r.rating, 0) / total).toFixed(1)
        : 0,
    stats,
  };
}
